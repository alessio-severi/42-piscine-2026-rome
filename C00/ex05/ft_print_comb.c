#include <unistd.h>

void	ft_print_comb(void)
{
	char	limit;
	char	c[3];

	limit = '9' - 2;
	c[0] = '0' - 1;
	while (++c[0] <= limit)
	{
		c[1] = c[0];
		while (++c[1] <= limit + 1)
		{
			c[2] = c[1];
			while (++c[2] <= limit + 2)
			{
				write (1, &c[0], 1);
				write (1, &c[1], 1);
				write (1, &c[2], 1);
				if (!((c[0] == limit) && (c[1] == limit + 1)
						&& (c[2] == limit + 2)))
					write(1, ", ", 2);
			}
		}
	}
}
