#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	min;
	char	c;

	min = nb;
	if (min < 0)
	{
		write (1, "-", 1);
		min = -min;
	}
	if (min > 9)
		ft_putnbr(min / 10);
	c = (min % 10) + '0';
	write (1, &c, 1);
}
