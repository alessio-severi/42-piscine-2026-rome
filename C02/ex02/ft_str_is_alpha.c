int	ft_str_is_alpha(char *str)
{
	while (*str != '\0')
	{
		if ((*str >= 'a' && *str <= 'z')
			|| (*str >= 'A' && *str <= 'Z'))
		{
			str++;
			continue ;
		}
		else
			return (0);
	}
	return (1);
}
