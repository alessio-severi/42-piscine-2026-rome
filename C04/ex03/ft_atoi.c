int	ft_atoi(char *str)
{
	int	sgn;
	int	number;

	sgn = 1;
	number = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == 32)
		str++;
	while (*str == '-' || *str == '+')
		if (*(str++) == '-')
			sgn *= -1;
	while (*str >= '0' && *str <= '9')
		number = number * 10 + (*(str++) - '0');
	return (number * sgn);
}
